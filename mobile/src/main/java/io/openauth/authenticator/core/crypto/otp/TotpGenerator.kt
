package io.openauth.authenticator.core.crypto.otp

import java.time.Clock
import java.time.Duration
import java.time.Instant
import javax.inject.Inject

class TotpGenerator @Inject constructor(
    private val hotpGenerator: HotpGenerator,
    private val clock: Clock
) {

    fun generate(
        secret: ByteArray,
        digits: OtpDigits = OtpDigits.DEFAULT,
        algorithm: OtpAlgorithm = OtpAlgorithm.DEFAULT,
        period: OtpPeriod = OtpPeriod.DEFAULT,
        instant: Instant = clock.instant()
    ): String {
        val counter = instant.epochSecond / period.seconds
        return hotpGenerator.generate(
            secret = secret,
            counter = counter,
            digits = digits,
            algorithm = algorithm
        )
    }

    fun remainingTime(
        period: OtpPeriod = OtpPeriod.DEFAULT,
        instant: Instant = clock.instant()
    ): Duration {
        val elapsed = instant.epochSecond % period.seconds
        return Duration.ofSeconds(period.seconds - elapsed)
    }
}