package io.openauth.authenticator.core.database.dao

import androidx.room.*
import io.openauth.authenticator.core.database.entity.AccountTagCrossRef
import io.openauth.authenticator.core.database.entity.TagEntity
import kotlinx.coroutines.flow.Flow

@Dao
interface TagDao {

    @Query("SELECT * FROM tags ORDER BY name ASC")
    fun observeAll(): Flow<List<TagEntity>>

    @Query("SELECT * FROM tags WHERE id = :id LIMIT 1")
    suspend fun getById(id: String): TagEntity?

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insert(tag: TagEntity)

    @Update
    suspend fun update(tag: TagEntity)

    @Delete
    suspend fun delete(tag: TagEntity)

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun addAccountToTag(crossRef: AccountTagCrossRef)

    @Delete
    suspend fun removeAccountFromTag(crossRef: AccountTagCrossRef)

    @Query("DELETE FROM account_tag_cross_ref WHERE account_id = :accountId")
    suspend fun removeAllFromAccount(accountId: String)

    @Query(
        """
        SELECT tags.* FROM tags 
        INNER JOIN account_tag_cross_ref 
        ON tags.id = account_tag_cross_ref.tag_id 
        WHERE account_tag_cross_ref.account_id = :accountId
        """
    )
    suspend fun getTagsForAccount(accountId: String): List<TagEntity>
}