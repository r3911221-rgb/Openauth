package io.openauth.authenticator.core.crypto.backup

import io.openauth.authenticator.core.crypto.cipher.EncryptionResult
import io.openauth.authenticator.core.crypto.kdf.KdfAlgorithm

class BackupCipherSpec(
    val formatVersion: BackupFormatVersion,
    val kdfAlgorithm: KdfAlgorithm,
    salt: ByteArray,
    val encryptionResult: EncryptionResult
) : AutoCloseable {

    val salt: ByteArray = salt.copyOf()
    private var closed = false

    override fun equals(other: Any?): Boolean {
        return other is BackupCipherSpec &&
               formatVersion == other.formatVersion &&
               kdfAlgorithm == other.kdfAlgorithm &&
               salt.contentEquals(other.salt) &&
               encryptionResult == other.encryptionResult
    }

    override fun hashCode(): Int {
        var result = formatVersion.hashCode()
        result = 31 * result + kdfAlgorithm.hashCode()
        result = 31 * result + salt.contentHashCode()
        result = 31 * result + encryptionResult.hashCode()
        return result
    }

    override fun toString(): String {
        return "BackupCipherSpec(" +
               "version=${formatVersion.value}, " +
               "kdf=$kdfAlgorithm, " +
               "redacted)"
    }

    override fun close() {
        if (closed) return
        closed = true
        salt.fill(0)
        encryptionResult.close()
    }
}