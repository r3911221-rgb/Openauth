package io.openauth.authenticator.core.crypto.util

object Base32Encoder {

    private const val ALPHABET = "ABCDEFGHIJKLMNOPQRSTUVWXYZ234567"

    fun encode(data: ByteArray): String {
        require(data.isNotEmpty()) { "Data cannot be empty" }

        val result = StringBuilder((data.size * 8 + 4) / 5)
        var buffer = 0
        var bits = 0

        for (byte in data) {
            buffer = (buffer shl 8) or (byte.toInt() and 0xFF)
            bits += 8

            while (bits >= 5) {
                bits -= 5
                result.append(ALPHABET[(buffer shr bits) and 0x1F])
            }
        }

        if (bits > 0) {
            result.append(ALPHABET[(buffer shl (5 - bits)) and 0x1F])
        }

        return result.toString()
    }
}