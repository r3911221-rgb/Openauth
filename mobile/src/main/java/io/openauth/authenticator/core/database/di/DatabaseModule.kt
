package io.openauth.authenticator.core.database.di

import android.content.Context
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.android.qualifiers.ApplicationContext
import dagger.hilt.components.SingletonComponent
import io.openauth.authenticator.core.database.AppDatabase
import io.openauth.authenticator.core.database.dao.AccountDao
import io.openauth.authenticator.core.database.dao.BackupHistoryDao
import io.openauth.authenticator.core.database.dao.GroupDao
import io.openauth.authenticator.core.database.dao.IconDao
import io.openauth.authenticator.core.database.dao.TagDao
import io.openauth.authenticator.core.database.sqlcipher.SqlCipherDatabaseFactory
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
object DatabaseModule {

    @Provides
    @Singleton
    fun provideDatabase(
        @ApplicationContext context: Context,
        factory: SqlCipherDatabaseFactory
    ): AppDatabase {
        return factory.create(context)
    }

    @Provides
    fun provideAccountDao(database: AppDatabase): AccountDao = database.accountDao()

    @Provides
    fun provideTagDao(database: AppDatabase): TagDao = database.tagDao()

    @Provides
    fun provideGroupDao(database: AppDatabase): GroupDao = database.groupDao()

    @Provides
    fun provideIconDao(database: AppDatabase): IconDao = database.iconDao()

    @Provides
    fun provideBackupHistoryDao(database: AppDatabase): BackupHistoryDao = database.backupHistoryDao()
}