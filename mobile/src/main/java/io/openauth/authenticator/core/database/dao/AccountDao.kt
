package io.openauth.authenticator.core.database.dao

import androidx.room.*
import io.openauth.authenticator.core.database.entity.AccountEntity
import kotlinx.coroutines.flow.Flow

@Dao
interface AccountDao {

    @Query("SELECT * FROM accounts ORDER BY sort_position ASC")
    fun observeAll(): Flow<List<AccountEntity>>

    @Query("SELECT * FROM accounts ORDER BY sort_position ASC")
    suspend fun getAll(): List<AccountEntity>

    @Query("SELECT * FROM accounts WHERE id = :id LIMIT 1")
    suspend fun getById(id: String): AccountEntity?

    @Query(
        """
        SELECT * FROM accounts 
        WHERE issuer LIKE '%' || :query || '%' 
        OR account_name LIKE '%' || :query || '%' 
        ORDER BY sort_position ASC
        """
    )
    fun search(query: String): Flow<List<AccountEntity>>

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insert(account: AccountEntity)

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertAll(accounts: List<AccountEntity>)

    @Update
    suspend fun update(account: AccountEntity)

    @Delete
    suspend fun delete(account: AccountEntity)

    @Query("DELETE FROM accounts WHERE id = :id")
    suspend fun deleteById(id: String)

    @Query(
        """
        UPDATE accounts 
        SET counter = counter + 1, updated_at = :updatedAt 
        WHERE id = :id
        """
    )
    suspend fun incrementCounter(id: String, updatedAt: Long)

    @Query(
        """
        UPDATE accounts 
        SET is_favorite = :favorite, updated_at = :updatedAt 
        WHERE id = :id
        """
    )
    suspend fun setFavorite(id: String, favorite: Boolean, updatedAt: Long)

    @Query(
        """
        UPDATE accounts 
        SET last_used_at = :time, usage_count = usage_count + 1, updated_at = :time 
        WHERE id = :id
        """
    )
    suspend fun recordUsage(id: String, time: Long)

    @Query(
        """
        UPDATE accounts 
        SET sort_position = :position, updated_at = :updatedAt 
        WHERE id = :id
        """
    )
    suspend fun updateSortPosition(id: String, position: Int, updatedAt: Long)

    @Transaction
    suspend fun reorder(accounts: List<AccountEntity>, updatedAt: Long) {
        accounts.forEachIndexed { index, account ->
            updateSortPosition(account.id, index, updatedAt)
        }
    }

    @Query("SELECT COUNT(*) FROM accounts")
    suspend fun count(): Int
}